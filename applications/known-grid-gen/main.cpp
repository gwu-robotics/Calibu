#include <Eigen/Eigen>
#include <calibu/target/TargetGridDot.h>

const char* sUriInfo =
  "Usage:"
  "\tknown-grid-gen [output]\n"
  "Options:\n"
  "\toutput      Output EPS file. Default: target.eps\n"
  "Example:\n"
  "\tknown-grid-gen\n"
  "\tknown-grid-gen output.eps\n";

static inline Eigen::MatrixXi GoogleLargeGrid();

int main(int argc, char *argv[])
{
  std::string output = (argc > 1 ? argv[1] : "target.eps");

  if(output == "-h" || output == "-help" || output == "?") {
    std::cout << sUriInfo << std::endl;
    return -1;
  }

  const double grid_spacing = 0.03156; // m
  const double small_rad = 0.00635; // m
  const double large_rad = 0.00889; // m

  const Eigen::Vector2d offset(0,0);
  double pts_per_unit = 650;
  calibu::TargetGridDot target(grid_spacing, GoogleLargeGrid());
  target.SaveEPS(output, offset, small_rad, large_rad, pts_per_unit);

  return 0;
}

